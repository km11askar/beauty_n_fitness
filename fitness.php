        <h2>fitness</h2>
        
    
        <p>
          Fitness is defined as the quality or state of being fit and healthy.
          Around 1950, perhaps consistent with the Industrial Revolution and the treatise of World War II, the term "fitness" increased in western vernacular by a factor of ten.
          The modern definition of fitness describes either a person or machine's ability to perform a specific function or a holistic definition of human adaptability to cope with various situations. 
          This has led to an interrelation of human fitness and physical attractiveness that has mobilized global fitness and fitness equipment industries. <br>
          Regarding specific function, fitness is attributed to persons who possess significant aerobic or anaerobic ability (i.e., endurance or strength).
          A well-rounded fitness program improves a person in all aspects of fitness compared to practicing only one, such as only cardio/respiratory or only weight training.<br>

          A comprehensive fitness program tailored to an individual typically focuses on one or more specific skills, and on age or health-related needs such as bone health.
          Many sources also cite mental, social and emotional health as an important part of overall fitness.
          This is often presented in textbooks as a triangle made up of three points, which represent physical, emotional, and mental fitness.
          Physical fitness has been shown to have benefits in preventing ill health and assisting recovery from injury or illness. 
          Along with the physical health benefits of fitness, it has also been shown to have a positive impact on mental health as well by assisting in treating anxiety and depression.
          Physical fitness can also prevent or treat many other chronic health conditions brought on by unhealthy lifestyle or aging as well and has been listed frequently as one of the most popular and advantageous self-care therapies.
          Working out can also help some people sleep better by building up sleeping pressure and possibly alleviate some mood disorders in certain individuals.<br>

          Developing research has demonstrated that many of the benefits of exercise are mediated through the role of skeletal muscle as an endocrine organ.
          That is, contracting muscles release multiple substances known as myokines, which promote the growth of new tissue, tissue repair, and various anti-inflammatory functions, which in turn reduce the risk of developing various inflammatory diseases.
          <br>

          there are two types of fitness exercises,<br><br>
           Aerobic exercises-Aerobic exercise, which improves cardiorespiratory fitness and increase stamina, involves movement that increases the heart rate to improve the body's oxygen consumption.
                             This form of exercise is an important part of all training regiments, whether for professional athletes or for the everyday person.<br>eg:-jogging, walking, working a treadmill, swimming cycling<br> 
           Unaerobic exercises- this is a fast, high-intensity exercise that does not require the body to utilize oxygen to produce energy.<br>
                                this helps to promote strength, endurance, speed, and power; and is used by bodybuilders to build workout intensity.<br>
                                Unaerobic exercises are thought to increase the metabolic rate, thereby allowing one to burn additional calories as the body recovers from exercise due to an increase in body temperature and excess post-exercise oxygen consumption after the exercise ended.
           <br> eg:- Weight training, Isometric exercise, Sprinting 
            
